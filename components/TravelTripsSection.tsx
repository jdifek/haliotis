// ─── Main section ─────────────────────────────────────────────────────────────

const ITEMS_PER_PAGE = 3;
export const TravelTripsSection: React.FC<Props> = ({
  tripCards = [], // пустой массив по умолчанию
  className,
}) => {
  const [currentPage, setCurrentPage] = useState(1);

  const totalPages = Math.ceil(tripCards.length / ITEMS_PER_PAGE);
  const start = (currentPage - 1) * ITEMS_PER_PAGE;
  const visible = tripCards.slice(start, start + ITEMS_PER_PAGE);

  return (
    <section
      className={`bg-[#f1f1f1] px-4 pb-12 pt-8 md:px-[30px] lg:px-[188px] md:pb-[50px] md:pt-[46px] ${
        className ?? ""
      }`}
    >
      <div className="flex flex-col gap-5 items-center justify-center">
        {visible.map((card, i) => (
          <TripCard key={`${card.locationId}-${start + i}`} card={card} />
        ))}
      </div>

      {totalPages > 1 && (
        <div className="mt-8 flex justify-center">
          <Pagination
            totalPages={totalPages}
            currentPage={currentPage}
            onPageChange={setCurrentPage}
          />
        </div>
      )}
    </section>
  );
};
